import Foundation

/// HTTP method definitions.
public enum HTTPMethod: String {
    case options = "OPTIONS"
    case get     = "GET"
    case post    = "POST"
    case put     = "PUT"
    case patch   = "PATCH"
    case delete  = "DELETE"
    case trace   = "TRACE"
    case connect = "CONNECT"
}
